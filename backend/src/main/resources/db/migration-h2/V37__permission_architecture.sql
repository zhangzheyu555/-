alter table auth_user add column if not exists permission_version bigint not null default 1;
alter table auth_token add column if not exists permission_version bigint not null default 1;

create table if not exists permission_catalog (
  permission_code varchar(120) not null primary key,
  module_code varchar(40) not null,
  permission_name varchar(120) not null,
  description varchar(500) null,
  risk_level varchar(16) not null default 'LOW',
  enabled tinyint(1) not null default 1,
  sort_order int not null default 0,
  created_at timestamp not null default current_timestamp,
  updated_at timestamp null default null,
  constraint chk_permission_catalog_risk check (risk_level in ('LOW', 'MEDIUM', 'HIGH'))
);

create index if not exists idx_permission_catalog_module
  on permission_catalog(module_code, sort_order);
create index if not exists idx_permission_catalog_enabled
  on permission_catalog(enabled, module_code);

create table if not exists user_permission_override (
  id bigint generated by default as identity primary key,
  tenant_id bigint not null,
  user_id bigint not null,
  permission_code varchar(120) not null,
  effect varchar(16) not null,
  created_by bigint null,
  created_at timestamp not null default current_timestamp,
  updated_at timestamp null default null,
  constraint uk_user_permission_override unique (tenant_id, user_id, permission_code),
  constraint chk_user_permission_override_effect check (effect in ('ALLOW', 'DENY')),
  constraint fk_user_permission_override_tenant foreign key (tenant_id) references tenant(id),
  constraint fk_user_permission_override_user foreign key (user_id) references auth_user(id) on delete cascade,
  constraint fk_user_permission_override_permission foreign key (permission_code) references permission_catalog(permission_code),
  constraint fk_user_permission_override_creator foreign key (created_by) references auth_user(id) on delete set null
);

create index if not exists idx_user_permission_override_effect
  on user_permission_override(tenant_id, user_id, effect);
create index if not exists idx_user_permission_override_permission
  on user_permission_override(permission_code);

create table if not exists user_data_scope (
  id bigint generated by default as identity primary key,
  tenant_id bigint not null,
  user_id bigint not null,
  domain_code varchar(40) not null,
  scope_type varchar(32) not null,
  scope_value_json longtext null,
  created_by bigint null,
  created_at timestamp not null default current_timestamp,
  updated_at timestamp null default null,
  constraint uk_user_data_scope unique (tenant_id, user_id, domain_code),
  constraint chk_user_data_scope_domain check (
    domain_code in ('STORE', 'FINANCE', 'SALARY', 'WAREHOUSE', 'INSPECTION', 'EXAM', 'PLATFORM')
  ),
  constraint chk_user_data_scope_type check (
    scope_type in ('ALL', 'STORE_LIST', 'OWN_STORE', 'NONE', 'CENTRAL_WAREHOUSE', 'SELF')
  ),
  constraint chk_user_data_scope_payload check (
    (scope_type = 'STORE_LIST' and scope_value_json is not null)
    or (scope_type <> 'STORE_LIST' and scope_value_json is null)
  ),
  constraint fk_user_data_scope_tenant foreign key (tenant_id) references tenant(id),
  constraint fk_user_data_scope_user foreign key (user_id) references auth_user(id) on delete cascade,
  constraint fk_user_data_scope_creator foreign key (created_by) references auth_user(id) on delete set null
);

create index if not exists idx_user_data_scope_domain
  on user_data_scope(tenant_id, domain_code, scope_type);

