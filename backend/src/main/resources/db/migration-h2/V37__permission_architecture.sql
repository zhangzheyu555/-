update business_todo
set assignee_role = case
      when upper(assignee_role) in ('SUPERVISOR', 'OPS') then 'OPERATIONS'
      else assignee_role
    end,
    review_role = case
      when upper(review_role) in ('SUPERVISOR', 'OPS') then 'OPERATIONS'
      else review_role
    end,
    updated_at = current_timestamp
where status not in ('COMPLETED', 'REJECTED')
  and (
    upper(assignee_role) in ('SUPERVISOR', 'OPS')
    or upper(review_role) in ('SUPERVISOR', 'OPS')
  );

update todo_escalation
set source_role = 'OPERATIONS'
where status <> 'RESOLVED'
  and upper(source_role) in ('SUPERVISOR', 'OPS');

update auth_user
set role = case
      when upper(role) in ('ADMIN', 'OWNER') then 'BOSS'
      when upper(role) in ('SUPERVISOR', 'OPS') then 'OPERATIONS'
      else upper(role)
    end,
    store_id = case
      when upper(role) in ('ADMIN', 'OWNER') then null
      else store_id
    end
where upper(role) in ('ADMIN', 'OWNER', 'SUPERVISOR', 'OPS');

update auth_user set permission_version = permission_version + 1;
